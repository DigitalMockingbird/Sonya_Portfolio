$('#myModal1').on('hidden.bs.modal', function() {
    //removes and readds link to close video when modal is closed
    var $frame = $('iframe#close1');
    var vidsrc = $frame.attr('src');
    $frame.attr('src','');
    $frame.attr('src', vidsrc);
});

